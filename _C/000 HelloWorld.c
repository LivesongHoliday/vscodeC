#include <stdio.h> //打开一个工具库, 头文件

int main() //主函数, 程序的入口
{
    printf("Hello, world!\n"); //打印输出"Hello, world!"

    getchar(); //读取键盘输入

    putchar('A'); //输出字符'A'到屏幕

    return 0; //退出程序
}

